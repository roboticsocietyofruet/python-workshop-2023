# Declare an integer variable
num_int = 10
print("The integer is:", num_int)

# Declare a float variable
num_float = 10.123456
print("The float is:", num_float)

# Set precision for the float variable
# The format() function is used to format the float to 2 decimal places
num_float_precise = format(num_float, '.2f')
print("The float with precision is:", num_float_precise)

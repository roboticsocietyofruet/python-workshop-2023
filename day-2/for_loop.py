# Use a for loop to print hello world 5 times
# The range function generates a sequence of numbers starting from 1 and ending at 5 (exclusive).
# In each iteration of the loop, the variable 'i' takes on the value of the next number in the sequence.
for i in range(1, 6):
    # This line is executed for each iteration of the loop.
    print("Hello world")

# Use a for loop to print numbers from 1 to 5
# The range function generates a sequence of numbers starting from 1 and ending at 5 (exclusive).
# In each iteration of the loop, the variable 'i' takes on the value of the next number in the sequence.
for i in range(1, 6):
    # This line is executed for each iteration of the loop.
    # It prints a string that includes the current value of 'i'.
    print("The count is:", i)


# Use for loop to write a program that prints the multiplication table (from 1 to 10) of a number.
# The number is stored in the variable 'num'.
num = 2
for i in range(1, 11):
    print(num, "x", i, "=", num*i)

